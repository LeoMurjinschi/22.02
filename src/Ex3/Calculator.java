package Ex3;

public class Calculator {

    public static int adunare(int a, int b)
    {
        return a+b;
    }
    public static int scadere(int a, int b)
    {
        return a-b;
    }
    public static int impartire(int a, int b)
    {
        return a/b;
    }
    public static int inmultire(int a, int b)
    {
        return a*b;
    }
}
